// Declare a variable Sent1 and assign a string value to it
let Sent1 = "This is the beginning of the string";

// Declare a variable Sent2 and assign a string value to it
let Sent2 = " and this is the end of the string";

// Concatenate Sent1 and Sent2 into a single string and store it in a new variable
let concatenatedString = Sent1 + Sent2;

// Display the concatenated string in an alert box
window.alert(concatenatedString);  // This will pop up an alert with the concatenated string

// Display the concatenated string directly on the webpage using document.write
document.write(concatenatedString);  // This will write the concatenated string to the webpage